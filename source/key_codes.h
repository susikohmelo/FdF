# define KEY_ARROW_R 65363
# define KEY_ARROW_L 65361
# define KEY_ARROW_U 65362
# define KEY_ARROW_D 65364

# define KEY_PLUS 61
# define KEY_MINUS 45

# define KEY_1 49
# define KEY_2 50
# define KEY_3 51
#endif
